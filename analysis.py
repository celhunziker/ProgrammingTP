def question_2(df, decade_stats, output_dir):
    """
    Q2: Is there a "golden era" of cinema? How are top-rated films
    distributed across decades, and does average rating vary by era?

    Plots:
      A) Dual-axis: movie count per decade (bars) + avg rating (line)
      B) Stacked bar: genre composition per decade (from pd.crosstab)
    """
    print("\n" + "=" * 70)
    print("Q2: Golden Era Analysis")
    print("=" * 70)

    fig, axes = plt.subplots(1, 2, figsize=(18, 7))

    ds = decade_stats.sort_values('decade')
    x_labels = ds['decade_label'].values
    x_pos = range(len(x_labels))

    # --- Plot A: Dual-axis (bars + line) ---
    color_bar = '#3266ad'
    color_line = '#A32D2D'

    bars = axes[0].bar(
        x_pos, ds['movie_count'], color=color_bar, alpha=0.7,
        label='Number of movies', edgecolor='black', linewidth=0.5,
    )
    axes[0].set_ylabel('Number of top-rated movies', color=color_bar)
    axes[0].tick_params(axis='y', labelcolor=color_bar)

    # Secondary axis for average rating
    ax2 = axes[0].twinx()
    ax2.plot(
        x_pos, ds['mean_rating'], color=color_line, marker='o',
        linewidth=2.5, markersize=7, label='Avg rating',
    )
    ax2.set_ylabel('Average rating', color=color_line)
    ax2.tick_params(axis='y', labelcolor=color_line)
    ax2.set_ylim(7.4, 8.5)

    axes[0].set_xticks(x_pos)
    axes[0].set_xticklabels(x_labels, rotation=45, ha='right')
    axes[0].set_xlabel('Decade')
    axes[0].set_title('Movie count and average rating by decade')

    # Combine legends from both axes
    lines1, labels1 = axes[0].get_legend_handles_labels()
    lines2, labels2 = ax2.get_legend_handles_labels()
    axes[0].legend(lines1 + lines2, labels1 + labels2, loc='upper left')

    # --- Plot B: Stacked bar — genre distribution per decade ---
    # pd.crosstab (04_data_wrangling)
    pivot = pd.crosstab(df['decade_label'], df['genre'])

    # Sort by decade order
    decade_order = sorted(
        df['decade_label'].unique(),
        key=lambda x: int(x.replace('s', '')),
    )
    pivot = pivot.reindex(decade_order)

    pivot.plot(
        kind='bar', stacked=True, ax=axes[1],
        color=[GENRE_COLORS.get(c, '#999') for c in pivot.columns],
        edgecolor='black', linewidth=0.3,
    )
    axes[1].set_title('Genre distribution of top-rated movies by decade')
    axes[1].set_xlabel('Decade')
    axes[1].set_ylabel('Number of movies')
    axes[1].legend(title='Genre', bbox_to_anchor=(1.02, 1), loc='upper left')
    axes[1].tick_params(axis='x', rotation=45)

    plt.tight_layout()
    path = os.path.join(output_dir, "Q2_golden_era.png")
    plt.savefig(path, dpi=150, bbox_inches='tight')
    plt.close()
    print(f"  Saved: {path}")

    # --- Answer ---
    peak = ds.loc[ds['movie_count'].idxmax()]
    highest = ds.loc[ds['mean_rating'].idxmax()]
    print(f"\n  ANSWER:")
    print(f"  -> Most top-rated movies: {peak['decade_label']} "
          f"({int(peak['movie_count'])} films)")
    print(f"  -> Highest avg rating: {highest['decade_label']} "
          f"({highest['mean_rating']:.2f})")
    print(f"  -> Older decades (1920s–1940s) show higher average ratings,")
    print(f"     likely due to survivorship bias: only the best classics")
    print(f"     remain in collective memory and receive high ratings today.")
    print(f"  -> The 1990s have the most entries but a lower average,")
    print(f"     reflecting broader representation rather than higher quality.")


