def main():
    """
    Main pipeline:
      1. Load CSV data (pd.concat)
      2. Clean and wrangle (fillna, dropna, map, duplicated)
      3. Transform (pd.cut, groupby+agg, pd.crosstab)
      4. Visualize and answer questions
    """
    print("\n" + "=" * 70)
    print("  FILMAFFINITY DATA ANALYSIS")
    print("  Programming for Data Science - Final Project")
    print("=" * 70)

    # Step 1: Load
    df = load_data(DATA_DIR)

    # Step 2: Clean
    df = clean_data(df)

    # Step 3: Transform
    df, genre_stats, decade_stats, director_genre_ct = transform_data(df)

    # Step 4: Visualize and answer
    question_1(df, genre_stats, OUTPUT_DIR)
    question_2(df, decade_stats, OUTPUT_DIR)
    question_3(df, director_genre_ct, OUTPUT_DIR)

    # --- Final summary ---
    print("\n" + "=" * 70)
    print("  ANALYSIS COMPLETE")
    print("=" * 70)
