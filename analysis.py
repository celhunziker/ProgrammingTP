def load_data(data_dir):
    """
    Load all CSV files from Part 1 and combine into a single DataFrame.

    Uses pd.concat to concatenate multiple DataFrames, as covered in
    04_data_wrangling (Concatenating Data Sets).

    Parameters:
        data_dir (str): Path to directory with CSV files.

    Returns:
        pd.DataFrame: Combined DataFrame with all genres.
    """
    print("=" * 70)
    print("STEP 1: DATA LOADING (pd.concat)")
    print("=" * 70)

    csv_files = sorted(glob.glob(os.path.join(data_dir, "filmaffinity_*.csv")))

    if not csv_files:
        raise FileNotFoundError(f"No CSV files found in {data_dir}")

    # Read each CSV into a list of DataFrames
    dataframes = []
    for filepath in csv_files:
        df = pd.read_csv(filepath, encoding='utf-8-sig')
        genre = os.path.basename(filepath).replace(
            "filmaffinity_", "").replace(".csv", "")
        print(f"  Loaded: {os.path.basename(filepath)} "
              f"-> {len(df)} rows, genre='{genre}'")
        dataframes.append(df)

    # Concatenate all DataFrames (pd.concat from 04_data_wrangling)
    df = pd.concat(dataframes, ignore_index=True)

    print(f"\n  Combined: {df.shape[0]} rows x {df.shape[1]} columns")
    print(f"  Columns: {list(df.columns)}")

    return df


