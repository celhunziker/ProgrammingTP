def question_3(df, director_genre_ct, output_dir):
    """
    Q3: Which directors dominate across multiple genre rankings?
    Are they genre specialists or versatile across genres?

    Plots:
      A) Horizontal bar: top 15 directors by frequency, colored by
         number of genres (specialist vs versatile)
      B) Heatmap: director × genre appearance matrix
    """
    print("\n" + "=" * 70)
    print("Q3: Director Dominance")
    print("=" * 70)

    fig, axes = plt.subplots(1, 2, figsize=(18, 9))

    # --- Plot A: Horizontal bar chart ---
    # value_counts + sort_values (03_data_manipulation, 06_use_case)
    director_counts = df['director'].value_counts().head(15)
    top_directors = director_counts.index.tolist()

    # Count number of genres per director
    genres_per_director = df.groupby('director')['genre'].nunique()

    # Assign colors by versatility
    bar_colors = []
    for d in top_directors:
        n = genres_per_director.get(d, 1)
        if n >= 3:
            bar_colors.append('#534AB7')   # purple: 3+ genres
        elif n == 2:
            bar_colors.append('#1D9E75')   # teal: 2 genres
        else:
            bar_colors.append('#BA7517')   # amber: 1 genre

    y_pos = range(len(director_counts))
    axes[0].barh(
        y_pos, director_counts.values, color=bar_colors,
        edgecolor='black', linewidth=0.5,
    )
    axes[0].set_yticks(y_pos)
    axes[0].set_yticklabels(director_counts.index)
    axes[0].set_xlabel('Appearances in top-30 lists')
    axes[0].set_title('Top 15 directors by frequency in rankings')
    axes[0].invert_yaxis()

    # Add genre count + avg rating annotation
    for i, (count, director) in enumerate(
        zip(director_counts.values, top_directors)
    ):
        n_genres = genres_per_director.get(director, 1)
        avg_r = df[df['director'] == director]['rating'].mean()
        axes[0].text(
            count + 0.15, i,
            f'{n_genres} genres · avg {avg_r:.1f}',
            va='center', fontsize=9, color='#555',
        )

    # Legend for colors
    from matplotlib.patches import Patch
    legend_elements = [
        Patch(facecolor='#534AB7', label='3+ genres (versatile)'),
        Patch(facecolor='#1D9E75', label='2 genres'),
        Patch(facecolor='#BA7517', label='1 genre (specialist)'),
    ]
    axes[0].legend(handles=legend_elements, loc='lower right', fontsize=9)

    # --- Plot B: Heatmap (from pd.crosstab result) ---
    # Use the director_genre_ct from transform_data (pd.crosstab)
    heatmap_data = director_genre_ct.head(15)
    # Sort by total appearances
    heatmap_data = heatmap_data.loc[
        heatmap_data.sum(axis=1).sort_values(ascending=True).index
    ]

    im = axes[1].imshow(
        heatmap_data.values, cmap='YlOrRd', aspect='auto',
        vmin=0, vmax=heatmap_data.values.max(),
    )

    axes[1].set_xticks(range(len(heatmap_data.columns)))
    axes[1].set_xticklabels(heatmap_data.columns, rotation=45, ha='right')
    axes[1].set_yticks(range(len(heatmap_data.index)))
    axes[1].set_yticklabels(heatmap_data.index)
    axes[1].set_title('Director × Genre heatmap (appearances)')

    # Add text annotations inside cells
    for i in range(len(heatmap_data.index)):
        for j in range(len(heatmap_data.columns)):
            val = heatmap_data.values[i, j]
            if val > 0:
                axes[1].text(
                    j, i, str(int(val)),
                    ha='center', va='center', fontweight='bold',
                    fontsize=11,
                    color='white' if val >= 3 else 'black',
                )

    plt.colorbar(im, ax=axes[1], label='Appearances', shrink=0.8)

    plt.tight_layout()
    path = os.path.join(output_dir, "Q3_directors.png")
    plt.savefig(path, dpi=150, bbox_inches='tight')
    plt.close()
    print(f"  Saved: {path}")

    # --- Answer ---
    top = director_counts.index[0]
    top_count = director_counts.values[0]
    versatile = genres_per_director[genres_per_director >= 3].sort_values(
        ascending=False)

    print(f"\n  ANSWER:")
    print(f"  -> Most frequent director: {top} ({top_count} appearances)")
    print(f"  -> Most versatile (3+ genres):")
    for d, n in versatile.items():
        genres = df[df['director'] == d]['genre'].unique()
        print(f"      {d}: {n} genres ({', '.join(genres)})")
    specialist = genres_per_director[genres_per_director == 1]
    specialist_top = df[df['director'].isin(specialist.index)][
        'director'].value_counts().head(3)
    print(f"  -> Top specialists (1 genre only):")
    for d, c in specialist_top.items():
        g = df[df['director'] == d]['genre'].iloc[0]
        print(f"      {d}: {c} appearances, all in {g}")


