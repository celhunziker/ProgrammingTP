def create_q1_figure(df, genre_stats):
    """
    Q1: Which genre has the highest average rating, and how do
    rating distributions compare across genres?
    
    Plots:
      Box plot + Bar chart with error bars
    """
    print("\n  Generating Q1 Plotly figure...")
    genres_order = genre_stats.index.tolist()
    
    fig = make_subplots(rows=1, cols=2, subplot_titles=(
        'Rating distributions by genre',
        'Average rating by genre'
    ))
    
    # --- Plot A: Box plot ---
    for genre in genres_order:
        genre_df = df[df['genre'] == genre]
        fig.add_trace(
            go.Box(
                y=genre_df['rating'],
                name=genre,
                line_color='white',
                fillcolor=GENRE_COLORS[genre],
                opacity=0.8,
                boxpoints=False, # no individual points
                hoverinfo='y',   # show only y-stats (median, q1, q3, etc)
            ),
            row=1, col=1
        )
        
    # --- Plot B: Bar chart with error bars ---
    fig.add_trace(
        go.Bar(
            x=genres_order,
            y=genre_stats['mean_rating'],
            error_y=dict(type='data', array=genre_stats['std_rating'], visible=True),
            marker_color=[GENRE_COLORS[g] for g in genres_order],
            marker_line_color='black',
            marker_line_width=1,
            opacity=0.85,
            text=genre_stats['mean_rating'].apply(lambda x: f"{x:.2f}"),
            textposition='outside',
            hovertemplate="Genre: %{x}<br>Avg Rating: %{y:.2f}<br>Movies: %{customdata[0]}<extra></extra>",
            customdata=genre_stats[['movie_count']].values,
        ),
        row=1, col=2
    )

    fig.update_layout(
        template='plotly_dark',
        hovermode='closest',  # Only show box stats when hovering the box
        showlegend=False,
        yaxis=dict(title='Rating (0-10)', range=[7.3, 9.1]),
        yaxis2=dict(title='Average Rating', range=[7.0, 9.0]),
        height=600
    )
    
    return fig

def create_q2_figure(df, decade_stats):
    """
    Q2: Is there a "golden era" of cinema? How are top-rated films
    distributed across decades, and does average rating vary by era?
    
    Plots:
      Timeline dual-axis + Stacked area
    """
    print("  Generating Q2 Plotly figure...")
    ds = decade_stats.sort_values('decade')
    
    fig = make_subplots(rows=1, cols=2, 
                       subplot_titles=('Movie count and avg rating by decade', 
                                       'Genre distribution over time'),
                       specs=[[{"secondary_y": True}, {"secondary_y": False}]])
    
    # --- Plot A: Dual-axis Timeline ---
    fig.add_trace(
        go.Bar(
            x=ds['decade_label'],
            y=ds['movie_count'],
            name='Number of movies',
            marker_color='#3266ad',
            opacity=0.7,
            hovertemplate="Decade: %{x}<br>Total Movies: %{y}<extra></extra>"
        ),
        row=1, col=1, secondary_y=False
    )
    
    fig.add_trace(
        go.Scatter(
            x=ds['decade_label'],
            y=ds['mean_rating'],
            name='Avg rating',
            mode='lines+markers',
            line=dict(color='#A32D2D', width=3),
            marker=dict(size=8),
            hovertemplate="Decade: %{x}<br>Avg Rating: %{y:.2f}<extra></extra>"
        ),
        row=1, col=1, secondary_y=True
    )
    
    # --- Plot B: Stacked area chart ---
    # Prepare data for stacked area
    pivot = pd.crosstab(df['decade_label'], df['genre'])
    decade_order = sorted(df['decade_label'].unique(), key=lambda x: int(x.replace('s', '')))
    pivot = pivot.reindex(decade_order)
    
    for genre in pivot.columns:
        fig.add_trace(
            go.Scatter(
                x=pivot.index,
                y=pivot[genre],
                name=genre,
                mode='lines',
                stackgroup='one',
                fillcolor=GENRE_COLORS[genre],
                line=dict(width=0.5, color='black'),
                hovertemplate="Decade: %{x}<br>Genre: " + genre + "<br>Movies: %{y}<extra></extra>"
            ),
            row=1, col=2
        )
        
    fig.update_layout(
        template='plotly_dark',
        height=700, # Increased height to accommodate legend
        margin=dict(t=200),  # Much more generous top margin
        legend=dict(orientation="h", yanchor="bottom", y=1.2, xanchor="right", x=1)
    )
    fig.update_yaxes(title_text="Movies count", row=1, col=1, secondary_y=False)
    fig.update_yaxes(title_text="Average rating", range=[7.4, 8.5], row=1, col=1, secondary_y=True)
    fig.update_yaxes(title_text="Movies count", row=1, col=2)
    
    return fig

def create_q3_figure(df, director_genre_ct):
    """
    Q3: Which directors dominate across multiple genre rankings?
    Are they genre specialists or versatile across genres?
    
    Plots:
      Horizontal bar + Heatmap
    """
    print("  Generating Q3 Plotly figure...")
    director_counts = df['director'].value_counts().head(15)
    top_directors = director_counts.index.tolist()
    
    genres_per_director = df.groupby('director')['genre'].nunique()
    
    # Versatility array for hover
    versatility_info = []
    colors = []
    for d in top_directors:
        n = genres_per_director.get(d, 1)
        if n >= 3:
            colors.append('#534AB7')
            versatility_info.append('Versatile (3+ genres)')
        elif n == 2:
            colors.append('#1D9E75')
            versatility_info.append('2 genres')
        else:
            colors.append('#BA7517')
            versatility_info.append('Specialist (1 genre)')
            
    fig = make_subplots(rows=1, cols=2, 
                       subplot_titles=('Top 15 directors by frequency', 
                                       'Director x Genre Heatmap'))
    
    # --- Plot A: Horizontal Bar ---
    avg_ratings = [df[df['director'] == d]['rating'].mean() for d in top_directors]
    
    fig.add_trace(
        go.Bar(
            y=top_directors[::-1], # Reversed for top-down display
            x=director_counts.values[::-1],
            orientation='h',
            marker_color=colors[::-1],
            marker_line_color='black',
            marker_line_width=1,
            text=[f"{n} genres (avg {r:.1f})" for n, r in zip([genres_per_director.get(d,1) for d in top_directors][::-1], avg_ratings[::-1])],
            textposition='inside',
            insidetextanchor='end',
            customdata=versatility_info[::-1],
            hovertemplate="<b>%{y}</b><br>Appearances: %{x}<br>Style: %{customdata}<extra></extra>"
        ),
        row=1, col=1
    )
    
    # --- Plot B: Heatmap ---
    heatmap_data = director_genre_ct.head(15)
    heatmap_data = heatmap_data.loc[heatmap_data.sum(axis=1).sort_values().index]
    
    # Create hover text matrix showing movie titles
    hover_text = []
    for director in heatmap_data.index:
        row_text = []
        for genre in heatmap_data.columns:
            movies = df[(df['director'] == director) & (df['genre'] == genre)]['title'].tolist()
            if movies:
                row_text.append(f"<b>{director} - {genre}</b><br>" + "<br>".join(movies))
            else:
                row_text.append(f"No movies")
        hover_text.append(row_text)
        
    fig.add_trace(
        go.Heatmap(
            z=heatmap_data.values,
            x=heatmap_data.columns,
            y=heatmap_data.index,
            colorscale='YlOrRd',
            showscale=True,
            text=hover_text,
            hovertemplate="%{text}<extra></extra>",
            texttemplate="%{z}",
            textfont={"size": 12}
        ),
        row=1, col=2
    )
    
    fig.update_layout(
        template='plotly_dark',
        height=650,
        showlegend=False
    )
    fig.update_xaxes(title_text="Appearances in top-30 lists", row=1, col=1)
    
    return fig

