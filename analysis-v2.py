def clean_data(df):
    """
    Clean and wrangle the raw DataFrame.

    Techniques used (all from course slides):
      - isnull().sum()   -> detect missing values
      - fillna()         -> fill missing text fields
      - dropna()         -> drop rows without essential fields
      - astype()         -> convert data types
      - map()            -> standardize genre labels
      - duplicated()     -> detect movies appearing in multiple genres

    Parameters:
        df (pd.DataFrame): Raw combined DataFrame.

    Returns:
        pd.DataFrame: Cleaned DataFrame.
    """
    print("\n" + "=" * 70)
    print("STEP 2: DATA WRANGLING & CLEANING")
    print("=" * 70)

    # --- 2.1: Check for missing values (isnull from 05_data_transformation) ---
    print("\n  2.1 Missing values:")
    missing = df.isnull().sum()
    print(missing[missing > 0].to_string()
          if missing.sum() > 0 else "    None found")

    # --- 2.2: Handle missing values (fillna from 05_data_transformation) ---
    # Fill missing text fields with 'Unknown'
    for col in ['director', 'cast', 'country']:
        n_missing = df[col].isnull().sum()
        if n_missing > 0:
            df[col] = df[col].fillna('Unknown')
            print(f"  -> Filled {n_missing} missing '{col}' with 'Unknown'")

    # Drop rows without year (essential for decade analysis)
    rows_before = len(df)
    df = df.dropna(subset=['year'])
    dropped = rows_before - len(df)
    if dropped > 0:
        print(f"  -> Dropped {dropped} rows with missing year")

    # --- 2.3: Type conversion (astype from 03_data_manipulation) ---
    df['year'] = df['year'].astype(int)
    df['rating'] = pd.to_numeric(df['rating'], errors='coerce')
    df['votes'] = pd.to_numeric(df['votes'], errors='coerce').fillna(0).astype(int)
    print("  2.3 Type conversions: year->int, rating->float, votes->int")

    # --- 2.4: Standardize genre labels (map from 05_data_transformation) ---
    genre_labels = {
        'drama': 'Drama',
        'comedy': 'Comedy',
        'thriller': 'Thriller',
        'sci_fi': 'Sci-Fi',
        'horror': 'Horror',
    }
    df['genre'] = df['genre'].map(genre_labels)
    print(f"  2.4 Genre labels standardized: {list(df['genre'].unique())}")

    # --- 2.5: Detect duplicates (duplicated from 05_data_transformation) ---
    dupes = df[df.duplicated(subset=['title', 'year'], keep=False)]
    n_dupes = dupes['title'].nunique()
    print(f"\n  2.5 Duplicate detection: {n_dupes} movies appear in 2+ genres")
    if n_dupes > 0:
        for title in dupes['title'].unique()[:5]:
            genres = dupes[dupes['title'] == title]['genre'].tolist()
            print(f"      '{title}' -> {genres}")
        if n_dupes > 5:
            print(f"      ... and {n_dupes - 5} more")

    # --- Summary ---
    print(f"\n  Final: {df.shape[0]} rows x {df.shape[1]} columns")
    print(f"  Year range: {df['year'].min()} – {df['year'].max()}")
    print(f"  Rating range: {df['rating'].min():.1f} – {df['rating'].max():.1f}")

    return df


