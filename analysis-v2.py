# STEP 3: DATA TRANSFORMATION
#   - Derived column: decade     (arithmetic on year)
#   - pd.cut                     (05_data_transformation: Discretization)
#   - groupby + agg              (04_data_wrangling: Group-by Object)
#   - pd.crosstab                (04_data_wrangling)
#   - value_counts               (06_use_case: Data Arrangement)
# =============================================================================

def transform_data(df):
    """
    Create derived columns and aggregated views for analysis.

    Techniques used:
      - Derived column: decade = (year // 10) * 10
      - pd.cut: bin ratings into categories
      - groupby + agg: compute stats per genre and decade
      - pd.crosstab: director × genre matrix
      - value_counts: director frequency

    Parameters:
        df (pd.DataFrame): Cleaned DataFrame.

    Returns:
        tuple: (df, genre_stats, decade_stats, director_genre_ct)
    """
    print("\n" + "=" * 70)
    print("STEP 3: DATA TRANSFORMATION")
    print("=" * 70)

    # --- 3.1: Create decade column (derived from year) ---
    df['decade'] = (df['year'] // 10) * 10
    df['decade_label'] = df['decade'].astype(str) + 's'
    print(f"  3.1 Created 'decade' column: "
          f"{sorted(df['decade'].unique())}")

    # --- 3.2: Discretization with pd.cut (05_data_transformation) ---
    # Bin ratings into categories using pd.cut
    bins = [0, 7.5, 8.0, 8.5, 10]
    labels = ['Good (≤7.5)', 'Very Good (7.6–8.0)',
              'Excellent (8.1–8.5)', 'Outstanding (>8.5)']
    df['rating_category'] = pd.cut(df['rating'], bins=bins, labels=labels)
    print(f"  3.2 Rating categories (pd.cut):")
    # value_counts (06_use_case)
    cat_counts = df['rating_category'].value_counts()
    for cat, count in cat_counts.items():
        print(f"      {cat}: {count} movies")

    # --- 3.3: Genre statistics (groupby + agg from 04_data_wrangling) ---
    genre_stats = df.groupby('genre').agg(
        mean_rating=('rating', 'mean'),
        median_rating=('rating', 'median'),
        std_rating=('rating', 'std'),
        min_rating=('rating', 'min'),
        max_rating=('rating', 'max'),
        total_votes=('votes', 'sum'),
        avg_votes=('votes', 'mean'),
        movie_count=('title', 'count'),
    ).round(2)
    # sort_values (03_data_manipulation)
    genre_stats = genre_stats.sort_values('mean_rating', ascending=False)
    print(f"\n  3.3 Genre statistics (groupby + agg):")
    print(genre_stats.to_string())

    # --- 3.4: Decade statistics (groupby + agg) ---
    decade_stats = df.groupby(['decade', 'decade_label']).agg(
        mean_rating=('rating', 'mean'),
        movie_count=('title', 'count'),
        avg_votes=('votes', 'mean'),
    ).round(2).reset_index()
    decade_stats = decade_stats.sort_values('decade')
    print(f"\n  3.4 Decade statistics:")
    print(decade_stats.to_string(index=False))

    # --- 3.5: Director × Genre cross-tabulation (pd.crosstab) ---
    # pd.crosstab from 04_data_wrangling
    director_genre_ct = pd.crosstab(df['director'], df['genre'])

    # Filter to directors with 2+ total appearances
    director_totals = director_genre_ct.sum(axis=1)
    director_genre_ct = director_genre_ct[director_totals >= 2]
    director_genre_ct = director_genre_ct.loc[
        director_genre_ct.sum(axis=1).sort_values(ascending=False).index
    ]
    print(f"\n  3.5 Director × Genre crosstab "
          f"({len(director_genre_ct)} directors with 2+ appearances):")
    print(director_genre_ct.head(10).to_string())

    return df, genre_stats, decade_stats, director_genre_ct


